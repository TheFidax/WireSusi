void resetCVs(void) { 
    for(uint16_t i = 0; i < sizeof(FactoryDefaultCVsValue); ++i) {              // Per tutte le CVs disponibili
        uint8_t defaultValue = pgm_read_byte_near(FactoryDefaultCVsValue + i);  // Cerco il valore di default

        eeprom_update_byte((uint8_t*)i, defaultValue);                          // Aggiorno la cella di memoria con il valore di default
        
        wdt_reset();                                                            // Reset del WDT
    }
}


